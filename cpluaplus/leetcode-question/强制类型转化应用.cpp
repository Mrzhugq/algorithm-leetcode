#include<stdio.h>
int main()
{
	int a[4]={1,2,3,4};
	int *ptr1=(int*)(&a+1);
	int *ptr2=(int*)((int)a+1);
	
	printf("%x %x\n",ptr1[-1],*ptr2);
	return 0;
}
//ptr1指向a数组的下一个数组
//ptr1[-1] -> *(ptr1-1) -> 指向4 

//ptr2(指向1的第二个字节) 
//*ptr2: 小段存储：00000010 00000000 00000000 00000000 (二进制）    ->      (十六进制) 0x2000000 
//*ptr2: 大段存储：00000000 00000000 00000001 00000000 (二进制）    ->      (十六进制) 0x100  
